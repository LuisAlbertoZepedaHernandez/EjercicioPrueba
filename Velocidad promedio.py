#Calcula velocidad promedio, dado el tiempo y la distancia.

distancia = int(input("Introducir distancia en kilometros enteros: "))
tiempo = int(input("Introducir tiempo en horas enteras: "))

velocidad = distancia / tiempo
print("velocidad promedio:", velocidad, "km/hr")
